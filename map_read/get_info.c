#include "../so_long.h"

void	ft_spawnpoint(int *inf, char **tab)
{
	int	x;
	int	y;

	x = 0;
	y = 0;
	while (tab[y])
	{
		while (tab[y][x])
		{
			if (tab[y][x] == 'E')
			{
				inf[0] = x;
				inf[1] = y;
				return ;
			}
			x++;
		}
		x = 0;
		y++;
	}
}